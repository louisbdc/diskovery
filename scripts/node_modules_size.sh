#!/usr/bin/env bash
#
# node_modules_size.sh
# Recherche récursivement tous les dossiers node_modules à partir d'un répertoire
# et calcule leur poids total. Peut aussi supprimer les node_modules trop anciens.
#
# Usage : ./node_modules_size.sh [répertoire] [options]
#
# Options :
#   --delete       Supprime les node_modules modifiés il y a plus de N jours
#                  (après affichage de la liste et confirmation).
#   --days N       Seuil d'ancienneté en jours (défaut : 30, soit ~1 mois).
#   -h, --help     Affiche cette aide.
#
# Exemples :
#   ./node_modules_size.sh ~/code
#   ./node_modules_size.sh ~/code --delete
#   ./node_modules_size.sh ~/code --delete --days 60

set -euo pipefail

ROOT="."
DELETE=false
DAYS=30

usage() {
  sed -n '2,20p' "$0" | sed 's/^# \{0,1\}//'
}

while [[ $# -gt 0 ]]; do
  case "$1" in
    --delete) DELETE=true; shift ;;
    --days)
      DAYS="${2:-}"
      if ! [[ "$DAYS" =~ ^[0-9]+$ ]]; then
        echo "Erreur : --days attend un nombre de jours." >&2
        exit 1
      fi
      shift 2
      ;;
    -h|--help) usage; exit 0 ;;
    -*) echo "Erreur : option inconnue '$1'." >&2; exit 1 ;;
    *) ROOT="$1"; shift ;;
  esac
done

if [[ ! -d "$ROOT" ]]; then
  echo "Erreur : '$ROOT' n'est pas un répertoire valide." >&2
  exit 1
fi

# Convertit une taille en Ko (blocs de 1024 octets) en format lisible.
human_readable() {
  awk -v kb="$1" 'BEGIN {
    split("Ko Mo Go To Po", units, " ")
    size = kb
    i = 1
    while (size >= 1024 && i < 5) { size /= 1024; i++ }
    printf "%.2f %s", size, units[i]
  }'
}

total_kb=0
count=0
old_total_kb=0
old_dirs=()

# -prune : on ne descend pas DANS un node_modules trouvé, ce qui évite de
# compter deux fois les node_modules imbriqués (leur poids est déjà inclus
# dans celui du node_modules parent).
while IFS= read -r -d '' dir; do
  size_kb=$(du -sk "$dir" | cut -f1)
  total_kb=$((total_kb + size_kb))
  count=$((count + 1))

  marker=""
  # Test sur la mtime du dossier node_modules lui-même : modifié il y a plus
  # de $DAYS jours (-maxdepth 0 : on ne teste que le dossier, pas son contenu).
  if $DELETE && [[ -n "$(find "$dir" -maxdepth 0 -mtime +"$DAYS" 2>/dev/null)" ]]; then
    old_dirs+=("$dir")
    old_total_kb=$((old_total_kb + size_kb))
    marker="  [+${DAYS}j -> candidat suppression]"
  fi

  printf "%10s  %s%s\n" "$(human_readable "$size_kb")" "$dir" "$marker"
done < <(find "$ROOT" -type d -name node_modules -prune -print0 2>/dev/null | sort -z)

echo "-------------------------------------------"
echo "node_modules trouvés : $count"
echo "Poids total          : $(human_readable "$total_kb")"

if ! $DELETE; then
  exit 0
fi

echo
if [[ ${#old_dirs[@]} -eq 0 ]]; then
  echo "Aucun node_modules modifié il y a plus de ${DAYS} jours. Rien à supprimer."
  exit 0
fi

echo "${#old_dirs[@]} dossier(s) node_modules modifié(s) il y a plus de ${DAYS} jours,"
echo "soit $(human_readable "$old_total_kb") à libérer :"
for d in "${old_dirs[@]}"; do
  echo "  - $d"
done
echo

read -r -p "Confirmer la suppression de ces dossiers ? [o/N] " reply || reply=""
if [[ "$reply" != "o" && "$reply" != "O" ]]; then
  echo "Suppression annulée. Aucun dossier supprimé."
  exit 0
fi

freed_kb=0
for d in "${old_dirs[@]}"; do
  # Garde-fous : non vide, dossier existant, et nom finissant bien par node_modules.
  if [[ -n "$d" && -d "$d" && "$d" == *node_modules ]]; then
    rm -rf -- "$d"
    echo "Supprimé : $d"
  else
    echo "Ignoré (chemin suspect) : $d" >&2
  fi
done

echo "-------------------------------------------"
echo "Espace libéré : $(human_readable "$old_total_kb")"
